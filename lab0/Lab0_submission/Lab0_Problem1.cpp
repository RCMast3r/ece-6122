#include <iostream>

int main()
{
    std::cout << "My name is: Ben Hall" << std::endl;
    std::cout << "This (\") is a double quote." << std::endl;
    std::cout << "This (`) is a single quote." << std::endl;
    std::cout << "This (\\) is a backslash." << std::endl;
    std::cout << "This (/) is a forward slash." << std::endl;
    return 0;
}